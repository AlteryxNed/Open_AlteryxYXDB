namespace SRC 
{
	struct RecordData
	{ 
	};

	inline char * ToCharP(RecordData *p)
	{
		return reinterpret_cast<char *>(p);
	}
	inline const char * ToCharP(const RecordData *p)
	{
		return reinterpret_cast<const char *>(p);
	}
}
